public class Light {

    public void turnon(){
        System.out.println("the light is on.");
    }
    public void turnoff(){
        System.out.println("the light is off.");
    }
    public void dimup(){
        System.out.println("the light is dimming up.");
    }
    public void dimdown(){
        System.out.println("the light is dimming down.");
    }


}
